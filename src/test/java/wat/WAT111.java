	@BeforeTest
	public void beforeTest() throws Exception {
		extent = ExtentManager.getReporter(filePath);
		rowData = testcase.get(this.getClass().getSimpleName());
		test = extent.startTest(rowData.getTestcaseId(), rowData.getTestcaseDescription()).assignCategory("WAT");
	}

	/**
	 * Method for login into WAT application using Steam ID
	 * 
	 * @throws Exception,
	 *             When WAT Login is not done
	 */
	@Test
	@Parameters({ "username", "password" })
	public void testLoginWATApp(String username, String password) throws Exception {

		boolean testRunmode = getTestRunMode(rowData.getTestcaseRunmode());
		boolean master_condition = suiteRunmode && testRunmode;
		logger.info("checking master condition status-->" + this.getClass().getSimpleName() + "-->" + master_condition);

		if (!master_condition) {
			status = 3;
			test.log(LogStatus.SKIP,
					"Skipping test case " + this.getClass().getSimpleName() + " as the run mode is set to NO");
			throw new SkipException("Skipping Test Case" + this.getClass().getSimpleName() + " as runmode set to NO");// reports
		}

		test.log(LogStatus.INFO, this.getClass().getSimpleName() + " execution starts.... ");

		try {
			openBrowser();
			clearCookies();
			maximizeWindow();
			test.log(LogStatus.INFO, "Login to WAT Applicaton using valid WAT Entitled user ");
			ob.navigate().to(host + CONFIG.getProperty("appendWATAppUrl"));
			pf.getLoginTRInstance(ob).loginToWAT(username, password, test);

		} catch (Throwable t) {
			logFailureDetails(test, t, "Login Fail", "login_fail");
			pf.getBrowserActionInstance(ob).closeBrowser();
		}

	}

	/**
	 * Method to Verify that Add alternative name icon is gray until the name has been entered in each name module
	 * 
	 * @throws Exception,
	 *             When Something unexpected
	 * 
	 */
	@Test(dependsOnMethods = { "testLoginWATApp" })
	@Parameters({ "LastName"})
	public void testAddAltNameFunctionality(String LastName) throws Exception {

		// NOTE - Assertion ERRORs are caught in EXCEPTION block just for
		// Reporting purpose
		try {
		
			Assert.assertEquals(pf.getBrowserActionInstance(ob)
						.getElement(OnePObjectMap.WAT_ADD_ALT_NAME_BTN_TEXT_XPATH).getAttribute("disabled"), "true","Add alternate button is not disabled in the beginning");
			pf.getBrowserActionInstance(ob).click(OnePObjectMap.WAT_AUTHOR_LASTNAME_XPATH);
			pf.getSearchAuthClusterPage(ob).enterAuthorLastName(LastName, test);
			if(pf.getBrowserActionInstance(ob)
					.getElement(OnePObjectMap.WAT_ADD_ALT_NAME_BTN_TEXT_XPATH).getAttribute("disabled")!=null)
			Assert.assertTrue(false,"Add alternate button is disabled after enetring Lastname/First name");
			test.log(LogStatus.PASS, "Add alternative name icon is gray until the name has been entered in each name module");
				pf.getBrowserActionInstance(ob).closeBrowser();
		} catch (Exception e) {
				test.log(LogStatus.FAIL, "Add alternative name icon is not gray until the name has been entered in each name module");
				logFailureDetails(test, e, "Add alternative name icon is not gray until the name has been entered in each name module", "Add_alt_btn_error");
				pf.getBrowserActionInstance(ob).closeBrowser();
		}
	}

