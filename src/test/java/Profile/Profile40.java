	@BeforeTest
	public void beforeTest() throws Exception {
		extent = ExtentManager.getReporter(filePath);
		rowData = testcase.get(this.getClass().getSimpleName());
		test = extent.startTest(rowData.getTestcaseId(), rowData.getTestcaseDescription()).assignCategory("Profile");
	}

	@Test
	@Parameters({"username", "password"})
	public void testLoginTRAccount(String username,
			String password) throws Exception {

		boolean testRunmode = getTestRunMode(rowData.getTestcaseRunmode());
		boolean master_condition = suiteRunmode && testRunmode;
		logger.info("checking master condition status-->" + this.getClass().getSimpleName() + "-->" + master_condition);

		if (!master_condition) {
			status = 3;
			test.log(LogStatus.SKIP,
					"Skipping test case " + this.getClass().getSimpleName() + " as the run mode is set to NO");
			throw new SkipException("Skipping Test Case" + this.getClass().getSimpleName() + " as runmode set to NO");// reports
		}

		test.log(LogStatus.INFO, this.getClass().getSimpleName() + " execution starts ");

		try {
			openBrowser();
			clearCookies();
			maximizeWindow();
			ob.navigate().to(System.getProperty("host"));
			pf = new PageFactory();

			pf.getLoginTRInstance(ob).waitForTRHomePage();
			pf.getLoginTRInstance(ob).enterTRCredentials(username, password);
			pf.getLoginTRInstance(ob).clickLogin();
		} catch (Throwable e) {
			test.log(LogStatus.FAIL, "Error:" + e);
			ErrorUtil.addVerificationFailure(e);
			status = 2;// excel
			test.log(LogStatus.INFO, "Snapshot below: " + test.addScreenCapture(
					captureScreenshot(this.getClass().getSimpleName() + "_unable_to_find_others_profile")));
			closeBrowser();
		}

	}

	/**
	 * Method for find and follow others profile
	 * 
	 * @throws Exception
	 */
	@Test(dependsOnMethods = "testLoginTRAccount")
	@Parameters("profile")
	public void getOtherProfileWatchlistTab(String profile) throws Exception {
		try {
			test.log(LogStatus.INFO, "go to other users profile page");
			pf.getSearchProfilePageInstance(ob).enterSearchKeyAndClick(profile);
			pf.getSearchProfilePageInstance(ob).clickPeople();
			pf.getProfilePageInstance(ob).clickProfile();
			test.log(LogStatus.INFO, "Validate Watchlist tab exist for other profiles");
			pf.getProfilePageInstance(ob).otherProfileWatchlistTab();
			pf.getLoginTRInstance(ob).logOutApp();
			test.log(LogStatus.INFO, this.getClass().getSimpleName() + " execution ends ");
			closeBrowser();

		} catch (Throwable t) {
			test.log(LogStatus.FAIL, "Something Unexpected");
			// print full stack trace
			StringWriter errors = new StringWriter();
			t.printStackTrace(new PrintWriter(errors));
			test.log(LogStatus.INFO, errors.toString());
			ErrorUtil.addVerificationFailure(t);
			status = 2;// excel
			test.log(LogStatus.INFO, "Snapshot below: " + test
					.addScreenCapture(captureScreenshot(this.getClass().getSimpleName() + "_HCR_Profile_badge_fail")));
			closeBrowser();
		}
	}

